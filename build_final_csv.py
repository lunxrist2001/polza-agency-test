#!/usr/bin/env python
# -*- coding: utf-8 -*-
"""Build final CSV with all 54 companies and personalization."""

import csv

# Read companies from companies_final.csv
with open('companies_final.csv', 'r', encoding='utf-8') as f:
    reader = csv.DictReader(f)
    companies = list(reader)

# Read personalization from companies_personalized.csv
personalizations = {}
with open('companies_personalized.csv', 'r', encoding='utf-8') as f:
    reader = csv.DictReader(f)
    for row in reader:
        personalizations[row['\xd0\x9d\xd0\xb0\xd0\xb7\xd0\xb2\xd0\xb0\xd0\xbd\xd0\xb8\xd0\xb5']] = row.get('\xd0\x9f\xd0\xb5\xd1\x80\xd1\x81\xd0\xbe\xd0\xbd\xd0\xb0\xd0\xbb\xd0\xb8\xd0\xb7\xd0\xb0\xd1\x86\xd0\xb8\xd1\x8f', '')

# Write combined CSV with BOM for Excel/Google Sheets compatibility
with open('polza_companies_final.csv', 'w', encoding='utf-8-sig', newline='') as f:
    fieldnames = ['\xd0\x9d\xd0\xb0\xd0\xb7\xd0\xb2\xd0\xb0\xd0\xbd\xd0\xb8\xd0\xb5', '\xd0\xa1\xd0\xb0\xd0\xb9\xd1\x82', '\xd0\x98\xd0\xbc\xd1\x8f \xd0\xba\xd0\xbe\xd0\xbd\xd1\x82\xd0\xb0\xd0\xba\xd1\x82\xd0\xb0', 'Email', '\xd0\x94\xd0\xbe\xd0\xbb\xd0\xb6\xd0\xbd\xd0\xbe\xd1\x81\xd1\x82\xd1\x8c', '\xd0\x9f\xd0\xb5\xd1\x80\xd1\x81\xd0\xbe\xd0\xbd\xd0\xb0\xd0\xbb\xd0\xb8\xd0\xb7\xd0\xb0\xd1\x86\xd0\xb8\xd1\x8f']
    writer = csv.DictWriter(f, fieldnames=fieldnames, delimiter=',', quotechar='"', quoting=csv.QUOTE_ALL)
    writer.writeheader()

    for company in companies:
        name = company['\xd0\x9d\xd0\xb0\xd0\xb7\xd0\xb2\xd0\xb0\xd0\xbd\xd0\xb8\xd0\xb5']
        writer.writerow({
            '\xd0\x9d\xd0\xb0\xd0\xb7\xd0\xb2\xd0\xb0\xd0\xbd\xd0\xb8\xd0\xb5': name,
            '\xd0\xa1\xd0\xb0\xd0\xb9\xd1\x82': company['\xd0\xa1\xd0\xb0\xd0\xb9\xd1\x82'],
            '\xd0\x98\xd0\xbc\xd1\x8f \xd0\xba\xd0\xbe\xd0\xbd\xd1\x82\xd0\xb0\xd0\xba\xd1\x82\xd0\xb0': company['\xd0\x98\xd0\xbc\xd1\x8f \xd0\xba\xd0\xbe\xd0\xbd\xd1\x82\xd0\xb0\xd0\xba\xd1\x82\xd0\xb0'],
            'Email': company['Email'],
            '\xd0\x94\xd0\xbe\xd0\xbb\xd0\xb6\xd0\xbd\xd0\xbe\xd1\x81\xd1\x82\xd1\x8c': company['\xd0\x94\xd0\xbe\xd0\xbb\xd0\xb6\xd0\xbd\xd0\xbe\xd1\x81\xd1\x82\xd1\x8c'],
            '\xd0\x9f\xd0\xb5\xd1\x80\xd1\x81\xd0\xbe\xd0\xbd\xd0\xb0\xd0\xbb\xd0\xb8\xd0\xb7\xd0\xb0\xd1\x86\xd0\xb8\xd1\x8f': personalizations.get(name, '')
        })

print('Created polza_companies_final.csv with ' + str(len(companies)) + ' companies')
