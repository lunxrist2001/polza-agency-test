def save_cache():
    with open(CACHE_FILE, "w", encoding="utf-8") as f:
        json.dump(cache, f, ensure_ascii=False, indent=2)

def main():
    with open(COMPANIES_FILE, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        companies = list(reader)

    print(f"Загружено {len(companies)} компаний")

    # Добавляем персонализацию
    for i, company in enumerate(companies):
        name = company["Название"]
        print(f"[{i+1}/{len(companies)}] Обрабатываю: {name}")

        fact = search_fact(name, company["Сайт"])
        company["Персонализация"] = fact

        # Небольшая задержка чтобы не перегружать
        if i % 10 == 0:
            time.sleep(0.5)

    # Сохраняем результат
    fieldnames = ["Название", "Сайт", "Имя контакта", "Email", "Должность", "Персонализация"]

    with open(OUTPUT_FILE, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(companies)

    print(f"\nГотово! Результат сохранен в {OUTPUT_FILE}")
    print(f"Всего обработано: {len(companies)} компаний")

if __name__ == "__main__":
    main()
